package com.javaacademy.lessons.homework.homework3.ex5;

public class Animal {
    private final int heightTail;
    private final int getHeightBody;

    public Animal(int heightTail, int getHeightBody) {
        this.heightTail = heightTail;
        this.getHeightBody = getHeightBody;
    }

    public int getHeightTail() {
        return heightTail;
    }

    public int getGetHeightBody() {
        return getHeightBody;
    }
}
